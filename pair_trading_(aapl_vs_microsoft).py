# BACKTEST ENGINE

def backtest(df, trade_size=10000):

    df = df.copy()

    # ---------------------------------------------
    # Initialize Performance Columns
    # ---------------------------------------------
    df["PnL"] = 0.0
    df["Cumulative PnL"] = 0.0

    hedge_ratio = df["Hedge Ratio"].iloc[0]

    position = 0
    entry_price1 = None
    entry_price2 = None
    quantity1 = 0
    quantity2 = 0

    trade_log = []
    total_trades = 0
    winning_trades = 0
    winning_returns = []

    # ---------------------------------------------
    # Main Backtest Loop
    # ---------------------------------------------
    for i in range(1, len(df)):

        date = df.index[i]
        price1 = df["asset1_close"].iloc[i]
        price2 = df["asset2_close"].iloc[i]

        long_signal  = df["Long Signal"].iloc[i]
        short_signal = df["Short Signal"].iloc[i]
        exit_signal  = df["Exit Signal"].iloc[i]
        stop_signal  = df["Stop Loss Signal"].iloc[i]

        # ================= ENTRY =================
        if position == 0:

            if long_signal:
                position = 1
                total_trades += 1

                entry_price1 = price1
                entry_price2 = price2

                quantity1 = trade_size / price1
                quantity2 = (trade_size * hedge_ratio) / price2

                trade_log.append(("ENTRY LONG", date, price1, price2))

            elif short_signal:
                position = -1
                total_trades += 1

                entry_price1 = price1
                entry_price2 = price2

                quantity1 = trade_size / price1
                quantity2 = (trade_size * hedge_ratio) / price2

                trade_log.append(("ENTRY SHORT", date, price1, price2))

        # ================= LONG EXIT =================
        elif position == 1 and (exit_signal or stop_signal):

            pnl = (
                (price1 - entry_price1) * quantity1 +
                (entry_price2 - price2) * quantity2
            )

            df.at[date, "PnL"] = pnl

            trade_return = pnl / trade_size

            if pnl > 0:
                winning_trades += 1
                winning_returns.append(trade_return)

            exit_type = "STOP LOSS LONG" if stop_signal else "EXIT LONG"
            trade_log.append((exit_type, date, pnl, trade_return))

            position = 0

        # ================= SHORT EXIT =================
        elif position == -1 and (exit_signal or stop_signal):

            pnl = (
                (entry_price1 - price1) * quantity1 +
                (price2 - entry_price2) * quantity2
            )

            df.at[date, "PnL"] = pnl

            trade_return = pnl / trade_size

            if pnl > 0:
                winning_trades += 1
                winning_returns.append(trade_return)

            exit_type = "STOP LOSS SHORT" if stop_signal else "EXIT SHORT"
            trade_log.append((exit_type, date, pnl, trade_return))

            position = 0

        df.at[date, "Position"] = position

    # ---------------------------------------------
    # Performance Metrics
    # ---------------------------------------------
    df["Cumulative PnL"] = df["PnL"].cumsum()

    final_pnl = df["Cumulative PnL"].iloc[-1]
    win_rate = (winning_trades / total_trades * 100) if total_trades > 0 else 0
    avg_return = (
        sum(winning_returns) / winning_trades * 100
        if winning_trades > 0 else 0
    )

    # ---------------------------------------------
    # Summary Output
    # ---------------------------------------------
    print("\n================ BACKTEST SUMMARY ================")
    print(f"Final P&L: ${final_pnl:,.2f}")
    print(f"Total Trades: {total_trades}")
    print(f"Winning Trades: {winning_trades}")
    print(f"Win Rate: {win_rate:.2f}%")
    print(f"Average Winning Return: {avg_return:.2f}%")

    print("\n================ TRADE LOG =======================")
    for trade in trade_log:
        print(trade)

    return df

